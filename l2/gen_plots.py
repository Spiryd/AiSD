sns.lineplot(data=df, x='n', y = 'swaps', hue='type')
plt.savefig('plots/quick_sort_comp_swaps.png')
plt.clf()
plt.title("quick_sort comp c/n")
sns.lineplot(data=df, x='n', y = 'c/n', hue='type')
plt.savefig('plots/quick_sort_comp_cn.png')
plt.clf()
plt.title("quick_sort comp s/n")
sns.lineplot(data=df, x='n', y = 's/n', hue='type')
plt.savefig('plots/quick_sort_comp_sn.png')
